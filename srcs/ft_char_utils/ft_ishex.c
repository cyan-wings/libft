int	ft_isdigit(int c);

int	ft_ishex(int c)
{
	if (ft_isdigit(c))
		return (1);
	if (c >= 'A' && c <= 'F')
		return (1);
	if (c >= 'a' && c <= 'f')
		return (1);
	return (0);
}
